package models;

public class User {

  private String emails;
  private String password;

  public void setEmails(String emails) {
      this.emails = emails;
  }

  public String getEmails() {
      return emails;
  }

  public void setPassword(String password) {
      this.password = password;
  }

  public String getPassword() {
      return password;
  }

}